// Parâmetros de Função

function soma(n1, n2) {
    return n1 + n2;
}

console.log(soma(3, 4));
console.log(soma(-150,33));
console.log(soma('eu', ' Juliana'));

// Os parâmetros seguem uma ordem; 

function nomeIdade(nome, idade) {
    return `Meu nome é ${nome} e tenho ${idade} anos de idade.`
   
}

console.log(nomeIdade('Juliana', 28));
